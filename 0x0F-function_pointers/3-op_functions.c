#include "3-calc.h"

/**
 * op_add -operation
 * @a: int
 * @b: int
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -operation
 * @a: int
 * @b: int
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul -operation
 * @a: int
 * @b: int
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -operation
 * @a: int
 * @b: int
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -operation
 * @a: int
 * @b: int
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}


